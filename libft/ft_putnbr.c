#include "libft.h"

void	ft_putnbr(int n)
{
	int	temp;

	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			n = -n;
			write(1, "-", 1);
		}
		temp = n % 10 + 48;
		if (n > 9)
			ft_putnbr(n / 10);
		write(1, &temp, 1);
	}
}
